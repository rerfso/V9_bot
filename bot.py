async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """Обработчик любого текстового сообщения (вопроса)."""
    user_text = update.message.text
    
    # Собираем ответ по методологии
    response = build_response(user_text)
    formatted = format_response(response)
    
    # Клавиатура с дополнительными действиями
    keyboard = [
        [InlineKeyboardButton("📋 Показать карту", callback_data="card")],
        [InlineKeyboardButton("🧭 Помощь", callback_data="help")],
    ]
    reply_markup = InlineKeyboardMarkup(keyboard)
    
    await update.message.reply_text(formatted, parse_mode="Markdown", reply_markup=reply_markup)

async def button_callback(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """Обработчик нажатий на кнопки."""
    query = update.callback_query
    await query.answer()
    
    if query.data == "card":
        await card_command(update, context)
    elif query.data == "help":
        await help_command(update, context)

