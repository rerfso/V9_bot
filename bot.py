def detect_level(text: str) -> str:
    """Определяет уровень вопроса по ключевым словам."""
    text_lower = text.lower()
    for level, keywords in LEVEL_KEYWORDS.items():
        for keyword in keywords:
            if keyword in text_lower:
                return level
    return "V"  # по умолчанию — человек (практика)

def detect_type(text: str) -> int:
    """Определяет тип вопроса по ключевым словам."""
    text_lower = text.lower()
    for qtype, keywords in TYPE_KEYWORDS.items():
        for keyword in keywords:
            if keyword in text_lower:
                return qtype
    return 1  # по умолчанию — прямой текст

def build_response(text: str) -> dict:
    """Собирает полный ответ на вопрос."""
    level = detect_level(text)
    qtype = detect_type(text)
    
    level_data = LEVELS.get(level, LEVELS["V"])
    type_data = QUESTION_TYPES.get(qtype, QUESTION_TYPES[1])
    
    response = {
        "level": level,
        "level_name": level_data["name"],
        "level_rule": level_data["rule"],
        "type": qtype,
        "type_name": type_data["name"],
        "type_sources": type_data["sources"],
        "type_rule": type_data["rule"],
        "practical_question": "Как это знание должно изменить твою жизнь?",
        "golden_rule": "Не переноси свойства творений на Аллаха. Не отрицай того, чем Аллах описал Себя. Не претендуй на знание Его Сущности.",
    }
    return response

